# Nhập độ dài cạnh đáy và chiều cao của tam giác 
do_dai_canh_day = float(input("Nhap do dai canh dai :"))
chieu_cao = float(input("Nhap chieu cao :"))
# Tính diện tích của tam giác 
dien_tich_cua_tam_giac = 0.5 * do_dai_canh_day * chieu_cao
# In ra kết quả diện tích tam giác 
print(f"Dien tich tam giac : {dien_tich_cua_tam_giac : }")